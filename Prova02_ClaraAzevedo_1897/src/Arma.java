public class Arma {
    private String nome;
    private int dano;
    private int durabilidade;

    public Arma(){}

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setDano(int dano) {
        this.dano = dano;
    }

    public void setDurabilidade(int durabilidade) {
        this.durabilidade = durabilidade;
    }
}